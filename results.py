from matplotlib import pyplot as plt


def plot_multiple(lines, x_label, y_label, title):
    for x, y, label in lines:
        plt.plot(x, y, label=label)
    plt.xlabel(x_label)
    plt.ylabel(y_label)
    plt.title(title)
    plt.legend()
    plt.show()


